# Definición de la variable edad
edad = 20

# Estructura de control if-else
if edad > 18:  # Verifica si la edad es mayor a 18
    print("Sos mayor de edad!")  # Se ejecuta si la condición es verdadera
elif edad > 7:  # Si no se cumple la condición anterior, verifica si es mayor a 7
    print("Sos mayor de 7 años")  # Se ejecuta si esta condición es verdadera
else:
    print("Adios.")  # Se ejecuta si ninguna de las condiciones anteriores es verdadera
